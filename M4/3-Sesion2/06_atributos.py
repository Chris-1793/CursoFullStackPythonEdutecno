# Definición de la clase Persona con atributos
class Persona:
    nombre = ''     # Atributo para almacenar el nombre de la persona
    escuela = ''    # Atributo para almacenar la escuela de la persona

# Creación de un objeto de la clase Persona
jorge = Persona()   # Crear una instancia de la clase Persona

# Asignación de valores a los atributos del objeto
jorge.nombre = 'Jorge'
jorge.escuela = 'Universidad de la vida'

# Imprimir los atributos del objeto
print(jorge.nombre)  # Salida: Jorge
print(jorge.escuela)  # Salida: Universidad de la vida
