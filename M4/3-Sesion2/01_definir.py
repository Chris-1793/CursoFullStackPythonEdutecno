# Definición de la clase Vehiculo
class Vehiculo:
    # Atributos de clase con valores por defecto
    modelo = ''           # Atributo para almacenar el modelo del vehículo
    marca = ''            # Atributo para la marca del vehículo
    color = ''            # Atributo que define el color del vehículo
    velocidad = 0         # Atributo que indica la velocidad actual del vehículo (inicialmente 0)
    nro_puertas = 0       # Atributo para el número de puertas del vehículo
    kilometraje = 0       # Atributo para el kilometraje del vehículo
    arrancando = False    # Atributo booleano para saber si el vehículo está encendido (False = apagado)
