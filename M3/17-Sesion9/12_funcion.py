# Definir la función sumar
def sumar(a, b):
    resultado = a + b  # Sumar los parámetros y almacenar el resultado
    return resultado  # Devolver el resultado

# Invocar la función con los valores 5 y 10
resultado = sumar(5, 10)

# Imprimir el resultado de la suma
print(resultado)

# Resultado esperado:
# 15
