# Usamos condicionales para verificar la temperatura y dar una recomendación
temperatura = 22

if temperatura > 30:
    print("Hace mucho calor, enciende el aire acondicionado.")
elif temperatura < 15:
    print("Hace frío, enciende la calefacción.")
else:
    print("La temperatura es agradable, no es necesario hacer nada.")
