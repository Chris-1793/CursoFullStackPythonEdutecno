# Ejemplos de convenciones de nombres en variables

# lowerCamelCase: la primera palabra comienza con minúscula, y las siguientes con mayúscula
miNombreCompleto = "Bastian Landskron"
edadUsuario = 33

# UpperCamelCase: todas las palabras comienzan con mayúscula
NombreCompleto = "Bastian Landskron"
EdadUsuario = 33

# snake_case: todas las palabras en minúsculas, separadas por guiones bajos
nombre_completo = "Bastian Landskron"
edad_usuario = 33

# SCREAMING_SNAKE_CASE: todas las letras en mayúsculas, separadas por guiones bajos
NOMBRE_COMPLETO = "BASTIAN LANDSKRON"
EDAD_USUARIO = 33

# Mostramos los valores de las variables
print(f"lowerCamelCase: {miNombreCompleto}, Edad: {edadUsuario}")
print(f"UpperCamelCase: {NombreCompleto}, Edad: {EdadUsuario}")
print(f"snake_case: {nombre_completo}, Edad: {edad_usuario}")
print(f"SCREAMING_SNAKE_CASE: {NOMBRE_COMPLETO}, Edad: {EDAD_USUARIO}")
