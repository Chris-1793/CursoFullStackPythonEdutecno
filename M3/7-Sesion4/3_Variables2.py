# Asignación simple
n = 100  # 'n' es una variable de tipo entero
print(n)  # Salida: 100

# Reasignación de una variable
n = "ahora soy una cadena"  # 'n' ahora es de tipo cadena
print(n)  # Salida: ahora soy una cadena

# Asignación encadenada
a = b = c = 300
print(a, b, c)  # Salida: 300 300 300
