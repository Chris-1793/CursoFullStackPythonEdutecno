conjunto1 = {1, 2, 3, 4}
conjunto2 = {3, 4, 5, 6}

# Unión
print(conjunto1 | conjunto2)  # Salida: {1, 2, 3, 4, 5, 6}

# Intersección
print(conjunto1 & conjunto2)  # Salida: {3, 4}

# Diferencia
print(conjunto1 - conjunto2)  # Salida: {1, 2}

# Diferencia simétrica
print(conjunto1 ^ conjunto2)  # Salida: {1, 2, 5, 6}
