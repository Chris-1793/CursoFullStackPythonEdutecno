# La multiplicación y la división tienen mayor prioridad que la suma y la resta
resultado = 2 + 3 * 5
print(resultado)
# Resultado esperado: 17 (3*5 se calcula primero, luego se suma 2)

# Uso de paréntesis para cambiar la precedencia
resultado_con_parentesis = (2 + 3) * 5
print(resultado_con_parentesis)
# Resultado esperado: 25 (2+3 se calcula primero, luego se multiplica por 5)
