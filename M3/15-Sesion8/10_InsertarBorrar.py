mi_lista = [10, 20, 30]
mi_lista.append(40)  # Insertar al final de la lista
print(mi_lista)

mi_lista.remove(20)  # Borrar un elemento por su valor
print(mi_lista)

mi_diccionario = {"nombre": "Carlos"}
mi_diccionario["edad"] = 30  # Agregar un nuevo par clave-valor
