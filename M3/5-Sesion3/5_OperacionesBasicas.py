# Suma
a = 10
b = 5
print("Suma: ", a + b)  # Resultado: 15

# Resta
print("Resta: ", a - b)  # Resultado: 5

# Multiplicación
print("Multiplicación: ", a * b)  # Resultado: 50

# División
print("División: ", a / b)  # Resultado: 2.0

# División entera
print("División entera: ", a // b)  # Resultado: 2

# Módulo (Resto de la división)
print("Módulo: ", a % b)  # Resultado: 0

# Potencia
print("Potencia: ", a ** b)  # Resultado: 100000
