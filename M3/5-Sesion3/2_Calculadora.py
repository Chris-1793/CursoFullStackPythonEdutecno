def calculadora():
    print("Calculadora Básica")
    a = float(input("Ingresa el primer número: "))
    b = float(input("Ingresa el segundo número: "))
    print(f"Suma: {a + b}")
    print(f"Resta: {a - b}")
    print(f"Multiplicación: {a * b}")
    print(f"División: {a / b}")
    print(f"División Entera: {a // b}")
    print(f"Módulo: {a % b}")
    print(f"Potencia: {a ** b}")

calculadora()
