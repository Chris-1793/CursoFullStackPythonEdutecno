// Ejercicio 6: Mostrar elementos de un array
// Este ejercicio muestra todos los elementos de un array usando un bucle.

var frutas = ["Manzana", "Banana", "Naranja", "Pera"]; // Se define un array con 4 elementos.

for (var i = 0; i < frutas.length; i++) { // El bucle recorre todo el array.
    console.log(frutas[i]); // Muestra cada elemento del array.
}
