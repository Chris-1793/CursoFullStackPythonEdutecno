// Ejercicio 2: Suma de dos números
// Este ejercicio pide al usuario que ingrese dos números y luego muestra la suma de esos números.

var numero1 = prompt("Ingresa el primer número:"); // Solicita el primer número.
var numero2 = prompt("Ingresa el segundo número:"); // Solicita el segundo número.
var suma = Number(numero1) + Number(numero2); // Suma los dos números. Se utiliza Number() para convertir el texto a número.
console.log("La suma de los dos números es: " + suma); // Muestra el resultado de la suma.
