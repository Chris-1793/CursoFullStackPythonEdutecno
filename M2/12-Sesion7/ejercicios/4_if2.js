// 'if' se utiliza para ejecutar un bloque de código solo si se cumple una condición específica.

let numero = 10;

if (numero > 5) { // Si la condición es verdadera, se ejecuta el siguiente bloque de código
    console.log("El número es mayor que 5"); // Salida: El número es mayor que 5
} else {
    console.log("El número es menor o igual a 5");
}
