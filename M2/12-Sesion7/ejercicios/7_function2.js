// 'function' se utiliza para declarar una función, que es un bloque de código reutilizable.

function saludar(nombre) { // Se define la función 'saludar' con un parámetro 'nombre'
    console.log("Hola, " + nombre + "!"); // Salida personalizada dependiendo del argumento
}

saludar("Carlos"); // Llamada a la función con el argumento "Carlos", Salida: Hola, Carlos!
