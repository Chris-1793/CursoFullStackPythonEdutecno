// La palabra clave 'return' se utiliza para salir de una función y devolver un valor al lugar donde se llamó la función.

function sumar(a, b) { // Declara una función llamada 'sumar' que toma dos parámetros 'a' y 'b'.
    return a + b; // Devuelve la suma de 'a' y 'b'.
}

let resultado = sumar(5, 7); // Llama a la función 'sumar' y almacena el resultado en la variable 'resultado'.
console.log("El resultado de la suma es: " + resultado); // Muestra el resultado.
// Salida: "El resultado de la suma es: 12"
