// La palabra clave 'if' se utiliza para ejecutar un bloque de código solo si se cumple una condición.

let temperatura = 30; // Declara una variable llamada 'temperatura' con el valor 30.

if (temperatura > 25) { // Si la condición 'temperatura > 25' es verdadera...
    console.log("Hace calor afuera."); // ...se ejecuta este bloque de código.
} else {
    console.log("El clima es agradable."); // Este bloque se ejecutaría si la condición fuera falsa.
}
