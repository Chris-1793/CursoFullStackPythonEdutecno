// 'const' se utiliza para declarar constantes en JavaScript. 
// Una vez que se asigna un valor a una constante, no puede ser reasignada.

const PI = 3.14159; // Declaración de la constante 'PI'
console.log(PI); // Salida: 3.14159

// PI = 3.14; // Esto daría un error porque no se puede reasignar una constante
