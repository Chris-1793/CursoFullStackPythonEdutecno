// La palabra clave 'function' se utiliza para declarar una función, que es un bloque de código que se puede reutilizar.

function saludar(nombre) { // Declara una función llamada 'saludar' que toma un parámetro 'nombre'.
    console.log("Hola, " + nombre + "!"); // Muestra un saludo personalizado.
}

saludar("Ana"); // Llama a la función 'saludar' con el argumento "Ana".
// Salida: "Hola, Ana!"
