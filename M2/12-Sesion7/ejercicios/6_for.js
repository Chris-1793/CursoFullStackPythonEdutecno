// La palabra clave 'for' se utiliza para repetir un bloque de código un número específico de veces.

for (let i = 0; i < 5; i++) { // El bucle comienza en i = 0, y se repite mientras 'i' sea menor que 5.
    console.log("Este es el bucle número " + i); // Muestra el valor de 'i' en cada iteración.
}

// Salida:
// Este es el bucle número 0
// Este es el bucle número 1
// Este es el bucle número 2
// Este es el bucle número 3
// Este es el bucle número 4
