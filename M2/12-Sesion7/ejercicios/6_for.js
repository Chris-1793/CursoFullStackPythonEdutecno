// La palabra clave 'for' se utiliza para repetir un bloque de código un número específico de veces.

for (let i = 0; i < 5; i++) { // El bucle comienza en i = 0, y se repite mientras 'i' sea menor que 5.
    console.log("Este es el bucle de la variable i y su número es " + i); // Muestra el valor de 'i' en cada iteración.
}

// Salida:
// Este es el bucle número 0 = i
// Este es el bucle número 0 + 1  i =  1 
// Este es el bucle número 1 + 1  i =  2
// Este es el bucle número 2 + 1    3
// Este es el bucle número 3 + 1    4
